public class lingkaran {
    //rumus luas lingkaran 
    int luasL(int y){
        int luas = y*y;
        return luas;
    }
    double luasL(double y){
        double luas = y*y;
        return luas;
    }
    
    //rumus keliling lingkaran
    int kelilingL(int y){
        int kel = 2*y;
        return kel;
    }
    double kelilingL(double y){
        double kel = 2*y;
        return kel;
    }
    
}
