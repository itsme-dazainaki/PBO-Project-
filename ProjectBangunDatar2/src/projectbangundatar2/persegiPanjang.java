public class persegiPanjang {
    //rumus luas persegi panjang
    int luasPP(int a, int b){
        return a*b;
    }    
    double luasPP(double a, int b){
        return a*b;
    }    
    double luasPP(int a, double b){
        return a*b;
    }        
    double luasPP(double a, double b){
        return a*b;
    }
    
    //rumus keliling persegi panjang
    int kelilingPP(int a, int b){
        return 2*(a+b);
    }    
    double kelilingPP(double a, int b){
        return 2*(a+b);
    }    
    double kelilingPP(int a, double b){
        return 2*(a+b);
    }        
    double kelilingPP(double a, double b){
        return 2*(a+b);
    }
}
